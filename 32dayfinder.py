no_of_day = int(input("Enter number of the day [1-7]: "))

if no_of_day == 1:
    print("Day is Sunday")
elif no_of_day == 2:
    print("Day is Monday")
elif no_of_day == 3:
    print("Day is Tuesday")
elif no_of_day == 4:
    print("Day is Wednesday")
elif no_of_day == 5:
    print("Day is Thursday")
elif no_of_day == 6:
    print("Day is Friday")
elif no_of_day == 7:
    print("Day is Saturday")
else:
    print("Invalid input")
